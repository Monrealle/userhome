#!/bin/bash
# userhome — выводит домашний каталог пользователя из /etc/passwd (или указанного файла)
# Использование: userhome [-f file] [username]

set -o nounset

# Функция вывода справки
show_help() {
    cat <<EOF
Usage: userhome [-f file] [username]

Выводит домашний каталог указанного пользователя из файла passwd.
Если имя пользователя не указано, используется текущий пользователь (\$USER).

Опции:
  -f file   использовать указанный файл вместо /etc/passwd
  --help    показать эту справку и выйти
  --usage   то же, что и --help

Если файл не найден — завершение с кодом 2.
Если пользователь не найден — завершение с кодом 1.
При успешном выполнении — код 0 и вывод только домашнего каталога.
EOF
}

# Значения по умолчанию
file="/etc/passwd"
username=""

# Разбор аргументов
while [[ $# -gt 0 ]]; do
    case "$1" in
        --help|--usage)
            show_help
            exit 0
            ;;
        -f)
            shift
            if [[ $# -eq 0 ]]; then
                echo "Ошибка: опция -f требует аргумент — имя файла" >&2
                show_help >&2
                exit 2
            fi
            file="$1"
            shift
            ;;
        -*)
            echo "Ошибка: неизвестная опция $1" >&2
            show_help >&2
            exit 2
            ;;
        *)
            # Позиционный аргумент — имя пользователя
            if [[ -n "$username" ]]; then
                echo "Ошибка: указано более одного имени пользователя" >&2
                show_help >&2
                exit 2
            fi
            username="$1"
            shift
            ;;
    esac
done

# Если имя пользователя не задано — берём текущего
if [[ -z "$username" ]]; then
    username="${USER:-$(whoami)}"
fi

# ASCII-логотип и информация о запуске (в stderr)
cat >&2 <<'EOF'
                        __                       
  __  __________  _____/ /_  ____  ____ ___  ___ 
 / / / / ___/ _ \/ ___/ __ \/ __ \/ __ `__ \/ _ \
/ /_/ (__  )  __/ /  / / / / /_/ / / / / / /  __/
\__,_/____/\___/_/  /_/ /_/\____/_/ /_/ /_/\___/
                                                    
EOF
echo >&2 "userhome: поиск домашнего каталога для '$username' в файле '$file'"

# Проверка существования файла
if [[ ! -f "$file" ]]; then
    echo "Ошибка: файл '$file' не найден" >&2
    exit 2
fi
if [[ ! -r "$file" ]]; then
    echo "Ошибка: файл '$file' недоступен для чтения" >&2
    exit 2
fi

# Поиск строки пользователя
line=$(grep "^${username}:" "$file" 2>/dev/null)
if [[ -z "$line" ]]; then
    echo "Ошибка: пользователь '$username' не найден в файле '$file'" >&2
    exit 1
fi

# Извлекаем домашний каталог (поле 6)
home_dir=$(echo "$line" | cut -d: -f6)

# Выводим результат
echo "$home_dir"
exit 0
